<?php

/* 
 * PHP type checking helper functions.
*/ 

$var = NULL;

if ( is_int($var) )
    print 'var is a integer';

if ( is_float($var) )
    print 'var is a float';

if ( is_bool($var) )
    print 'var is a boolean';

if ( is_string($var) )
    print 'var is a string';

if ( is_null($var) )
    print 'var is null';
 
